

CREATE TABLE BOOK (
    "BOOK_ID" NUMBER(10,0) PRIMARY KEY,
    "TITLE" VARCHAR2(100) NOT NULL,
    "AUTHOR" VARCHAR2(100),
    "PRICE" NUMBER(10,0),
    "PUBLISHER" VARCHAR2(20),
    "PUB_YEAR" DATE
);

CREATE TABLE RENTAL(
    "RENTAL_ID" NUMBER(10,0) PRIMARY KEY,
    "MEMBER_ID" NUMBER(10,0),
    "BOOK_ID"   NUMBER(10,0),
    "RENTDATE"  DATE,
    "RETURN_DATE"   DATE,
    CONSTRAINT FK_MEMBER FOREIGN KEY ("MEMBER_ID") REFERENCES BOOK_MEMBER("MEMBER_ID"),
    CONSTRAINT FK_BOOK FOREIGN KEY ("BOOK_ID") REFERENCES BOOK("BOOK_ID")
);

CREATE TABLE BOOK_MEMBER(
    "MEMBER_ID"  NUMBER(10,0) PRIMARY KEY,
    "NAME"  VARCHAR2(20) NOT NULL,
    "PHONE" VARCHAR2(20),
    "ADDRESS"   VARCHAR2(100)
);



